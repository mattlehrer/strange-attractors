<main>
	<p>
		This site is an exploration of chaotic systems and <a
			href="https://en.wikipedia.org/wiki/Attractor#Strange_attractor">strange attractors</a
		>. I was inspired to create it while reading
		<a href="https://openlibrary.org/works/OL15899W/Chaos?edition=key:/books/OL7960927M"
			>Chaos by James Gleick</a
		>.
	</p>
	<p>
		Each of the strange attractors is a system of three equations which are represented in 3D space
		as the calculations for x, y, and z which change over time. The first one was discovered by
		Edward Lorenz while trying to model weather patterns and is the inspiration for the term and
		concept of
		<a href="https://en.wikipedia.org/wiki/Butterfly_effect">the butterfly effect</a>. There are a
		few aspects that make these systems interesting.
	</p>
	<p>
		First, there is no way to predict values for some time in the future. The only way to calculate
		a future position is to iterate through the values, from moment to moment. There are rules which
		govern the system but an appearance of randomness.
	</p>
	<p>
		Second, the positions never repeat. As the dots move through space, they never cross the same
		exact point again. If they did, from that point forward the positions would repeat. There would
		be a loop and the system would be predictable.
	</p>
	<p>
		Third, the equations are sensitive to initial conditions in the extreme. The tiniest difference
		in initial values quickly results in a wildly different path through space. This is the meaning
		of the butterfly effect. A small difference here causes a big difference over there. This is a
		primary principle of <a href="https://en.wikipedia.org/wiki/Chaos_theory">chaos theory</a>,
		which can be summarized as: "the present determines the future, but the approximate present does
		not approximately determine the future."
	</p>
	<p>
		The visualizations of the attractors are interactive. You can drag them around and zoom in and
		out for different viewpoints. The grid on the landing page just shows one initial position and
		path moving over time. On each of the individual attractor pages, however, additional "dots" can
		be added, each with their own starting position and path. You can control the starting positions
		and colors of these.
	</p>
	<p>I hope you find it as beautiful, mesmerizing, and meditative to look at as I do.</p>
</main>

